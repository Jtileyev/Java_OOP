package Start.lesson_4;

public class Conditions {
    public static void main(String[] args) {
        int x = 5;
        int y = 6;

        if (x > y) {
            System.out.println("y is greater than x");
        } else if (x == y) {
            System.out.println("y is equal to x");
        } else {
            System.out.println("y is less than x");
        }
    }
}
